        blocks = (BlockType[][]) Array.newInstance(BlockType.class, blocksDef.length, blocksDef[0]
            .length);
        for (int i = 0; i < blocksDef.length; i++)
            for (int j = 0; j < blocksDef[i].length; j++)
                blocks[i][j] = BlockType.byId(blocksDef[i][j]);

